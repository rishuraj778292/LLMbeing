// Helper functions
export const getAllCategories = () => {
  return AI_CATEGORIES;
};

export const getCategoryByValue = (value) => {
  return AI_CATEGORIES.find(category => category.value === value);
};

export const getSkillsByCategory = (categoryValue) => {
  const category = getCategoryByValue(categoryValue);
  return category ? category.skills : [];
};

export const searchCategories = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return AI_CATEGORIES.filter(category => 
    category.label.toLowerCase().includes(term) ||
    category.description.toLowerCase().includes(term) ||
    category.skills.some(skill => skill.toLowerCase().includes(term))
  );
};

export const searchSkills = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return AI_SKILLS.filter(skill => 
    skill.toLowerCase().includes(term)
  );
};

export const getPopularSkills = (limit = 20) => {
  // Return most commonly used skills first
  return AI_SKILLS.slice(0, limit);
};

export const getCategoriesWithSkillCount = () => {
  return AI_CATEGORIES.map(category => ({
    ...category,
    skillCount: category.skills.length
  }));
};

export const getCategoryLabel = (categoryValue) => {
  const category = getCategoryByValue(categoryValue);
  return category ? category.label : categoryValue;
};